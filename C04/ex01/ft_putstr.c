#include <unistd.h>

void	wwrite(char ch)
{
	write(1, &ch, 1);
}

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		wwrite(str[i]);
		i++;
	}
}
