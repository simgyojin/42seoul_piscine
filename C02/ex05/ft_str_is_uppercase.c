int	ft_str_is_uppercase(char *str)
{
	int		i;
	int		temp;

	i = 0;
	temp = 1;
	while (str[i] != '\0')
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
		{
			temp = 1;
		}
		else
		{
			temp = 0;
			return (temp);
		}
		i++;
	}
	return (temp);
}
